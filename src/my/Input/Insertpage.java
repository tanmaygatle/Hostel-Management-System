    private void jButtonSubmit2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmit2ActionPerformed
        // TODO add your handling code here:
        if(txtempname.getText() == null || txtsalary.getText() == null || txtempphoneno.getText() == null) {
            JOptionPane.showMessageDialog(null,"Some fields are empty", "Access Denied",JOptionPane.ERROR_MESSAGE);
        }
        else if(txtempphoneno.getText().length() != 10 || txtempphoneno.getText().matches("[0-9]+") == false) {
            JOptionPane.showMessageDialog(null,"Phone no incorrect", "Access Denied",JOptionPane.ERROR_MESSAGE);            
        }
        else if(txtsalary.getText().matches("[0-9]+") == false) {
            JOptionPane.showMessageDialog(null,"Salary incorrect", "Access Denied",JOptionPane.ERROR_MESSAGE);
        }
        else{
            String sql = "insert into mess_employee(emp_name,emp_salary,emp_phoneno,block_id,emp_designation) values (?,?,?,?,?)";
            try{
                pst = conn.prepareStatement(sql);
                pst.setString(1,txtempname.getText());
                pst.setInt(2,Integer.valueOf(txtsalary.getText()));
                pst.setString(3,txtempphoneno.getText());
                pst.setString(4,txtempblockid.getSelectedItem().toString());
                pst.setString(5,txtempdes.getSelectedItem().toString());
                int i = pst.executeUpdate();
                if(i > 0){
                    JOptionPane.showMessageDialog(null,"You have successfully registered a mess employee");
                }
                else{
                    JOptionPane.showMessageDialog(null,JOptionPane.ERROR_MESSAGE);
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
            
    }//GEN-LAST:event_jButtonSubmit2ActionPerformed

